    	<div class="toggle" style="display:none">
    		<p>Los riesgos laborales en las empresas y en cualquier entorno de trabajo vienen provocados por los diferentes elementos que podemos encontrar en torno a los puestos de trabajo. Estos elementos son lo que denominamos condiciones de trabajo.</p>
    		<p>Estas condiciones de trabajo suponen, con frecuencia, riesgos potenciales, de ahí la necesidad de conocerlas para así determinar mejor los riesgos a que está expuesta la persona en su actividad diaria.</p>
