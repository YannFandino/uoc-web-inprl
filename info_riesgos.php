    <section class="seccion">
    	<h2>Riesgos</h2>
    	<div class="toggle" style="display:none">
    		Estos conceptos se nombraron con anterioridad, se dará una definición complementaria
			<h3>Peligro</h3>
			<p>fuente o situación con capacidad de daño en términos de lesiones, daños a la propiedad, daños al medio ambiente o una combinación de ellos.</p>
			<h3>Riesgo laboral</h3>
			<p>Posibilidad de que un trabajador sufra un determinado daño derivado del trabajo.</p>
			<h3>Factores de riesgo</h3>
			<p>Se toman en cuenta los siguientes factores:</p>
    		<ul>
    			<li>La probabilidad, que se analizará teniendo en cuenta la doble vertiente de: probabilidad de que ocurra un accidente, probabilidad de que se produzcan lesiones.</li>
				<li>La gravedad o severidad de los daños, que atenderá a la calificación de lesiones, para lo que se tendrán en cuenta las consecuencias que se prevea pueda provocar, o el número de trabajadores a los que puede afectar.</li>
				<li>La frecuencia con la que se repita el riesgo o el tiempo de exposición a que el trabajador o trabajadores estén expuestos.</li>
    		</ul>
		</div>
    </section>
