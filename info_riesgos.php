    	<div class="toggle" style="display:none">
			<p>La ley de prevención de riegos laborales en su artículo número 3 indica que, <q>la Ley tiene por objeto la determinación del cuerpo básico de garantías y responsabilidades preciso para establecer un adecuado nivel de protección de la
			salud de los trabajadores frente a los riesgos derivados de las condiciones de trabajo, y ello en el marco de una política coherente, coordinada y eficaz de prevención de los riesgos laborales.</q></p>
			<p>Para ello establecen en el artículo 4 una serie de conceptos, los mas importantes:</p>
			<ul>
				<dt><b>Prevención</b></dt><dd>Conjunto de actividades o medidas adoptadas o	previstas en todas las fases de actividad de la empresa con el fin de evitar o disminuir los riesgos derivados del trabajo.</dd><br>
				<dt><b>Riesgo laboral</b></dt><dd>La posibilidad de que un trabajador sufra un determinado daño derivado del trabajo. Para calificar un riesgo desde el punto de vista de su gravedad, se valorarán conjuntamente la probabilidad de que se produzca el daño y la severidad del mismo.</dd><br>
				<dt><b>Daños derivados del trabajo</b></dt><dd>Las enfermedades, patologías o lesiones sufridas con motivo u ocasión del trabajo.</dd><br>
				<dt><b>Equipo de trabajo</b></dt><dd>cualquier máquina, aparato, instrumento o instalación utilizada en el trabajo.</dd><br>
				<dt><b>Procesos, actividades, operaciones, equipos o productos potencialmente peligrosos</b></dt><dd>Aquellos que, en ausencia de medidas preventivas específicas, originen riesgos para la seguridad y la salud de los trabajadores que los desarrollan o utilizan.</dd><br>
				<dt><b>Condición de trabajo</b></dt><dd>Cualquier característica del mismo que pueda tener una influencia significativa en la generación de riesgos para la seguridad y la salud del trabajador.</dd><br>
			</ul>
