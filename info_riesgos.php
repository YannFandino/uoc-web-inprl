    <section class="seccion">
    	<h2>Daños derivados del trabajo</h2>
    	<div class="toggle" style="display:none">
    		<h3>Accidente de trabajo</h3>
    		<p>Se entiende por accidente de trabajo toda lesión corporal que el trabajador sufra con ocasión o a consecuencia del trabajo que ejecuta por cuenta ajena.</p>
    		<h3>Enfermedad profesional</h3>
    		<p>Es la pérdida de salud como consecuencia de lesiones de tipo orgánico o funcional que se desarrollan muy lentamente a causa de la realización de sus funciones laborales, de manera que el trabajador se siente realmente enfermo después de un período muy dilatado, a veces quince o veinte años de trabajo. A efectos legales reciben el nombre de "enfermedades del trabajo" y tendrán la consideración de "accidente de trabajo".</p>
			<p>La llamada higiene industrial constituye una técnica preventiva que pretende mejorar las condiciones ambientales del trabajo con el fin de reducir el riesgo de enfermedad profesional, pretendiendo que la salud del trabajador no se deteriore a causa de las condiciones en que desarrolla su trabajo.</p>
		</div>
    </section>
