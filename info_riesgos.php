    	<h2>Tipología de peligros</h2>
    	<div class="toggle" style="display:none">
    	<p>La tipología de peligros la podemos identificar de acuerdo a las condiciones de trabajo:</p>
    		<ul>
				<li>Mecánicas: todos los elementos y equipos del trabajo.</li>
				<li>Físicas: el lugar de trabajo, ambiente del mismo, condiciones ambientales, etc.</li>
				<li>Químicas: presencia de cualquier sustancia, ya sea en estado sólido, líquido o gaseoso.</li>
				<li>Biológicas: hongos, bacterias o virus.</li>
				<li>Psicológicas: condiciones que puedan influir sobre la mente de forma directa o indirecta.</li>
				<li>Sociales: se consideran aquí los aspectos humanos dentro de la empresa.</li>
			</ul>
		</div>
