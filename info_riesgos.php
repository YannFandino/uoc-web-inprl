    <section class="seccion">
    	<h2>Integración de la prevención en la empresa</h2>
    	<div class="toggle" style="display:none">
    		<p>Según el Reglamento de los Servicios de Prevención, la evaluación de los riesgos derivados del trabajo es el proceso dirigido a estimar a magnitud de aquellos riesgos que no hayan podido evitarse, obteniendo la información necesaria para que el empresario esté en condiciones de tomar una decisión apropiada sobre la necesidad de adoptar medidas preventivas y, en tal caso, sobre el tipo de medidas que deben adoptarse.</p>
    		<p>Por lo tanto, el empresario debe partir de una evaluación inicial de todos los puestos de trabajo, que deberá ser realizada por personal competente y que se revisará siempre que se produzcan modificaciones en los procesos de trabajo, equipos, materiales, o cuando se produzca un daño para un trabajador, etc.</p>
    		<p>También se aboga por considerar que deben adaptarse las condiciones de trabajo a los trabajadores y a sus características personales (físicas y psíquicas) de modo que se eliminen o se minimicen las situaciones de riesgo. Si bien nunca encontraremos dos trabajadores de características exactamente iguales, sí deberemos definir unas condiciones de trabajo adecuadas para cada trabajador considerado individualmente.</p>
    		<p>Además es importante que todos los afectados por los riesgos laborales tengan un papel activo en la gestión de la prevención de riesgos laborales. Los trabajadores de la empresa: los operarios, los mandos, los responsables de prevención, el empresario, la dirección, se ven afectados en mayor o menor medida por los riesgos laborales que se dan en el entorno de sus respectivos trabajos.</p>
		</div>
